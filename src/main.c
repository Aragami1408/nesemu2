static SDL_Color RGB(int r, int g, int b) {
	SDL_Color color;
	color.r = (Uint8) r;
	color.g = (Uint8) g;
	color.b = (Uint8) b;

	return color;
}

static SDL_Color color(u8 byte) {
	byte = (byte >= 9 && byte <= 14)? byte - 7: byte;

	switch(byte) {
		case 0: return RGB(0, 0, 0);
		case 1: return RGB(255, 255, 255);
		case 2: return RGB(128, 128, 128);
		case 3: return RGB(255, 0, 0);
		case 4: return RGB(0, 255, 0);
		case 5: return RGB(0, 0, 255);
		case 6: return RGB(255, 0, 255);
		case 7: return RGB(255, 255, 0);

		default: return RGB(0, 255, 255);
	}
}

static bool read_screen_state(cpu_t *cpu, u8 *frame) {
	int frame_idx = 0;
	bool update = false;

	for (u16 i = 0x0200; i < 0x600; i++) {
		u8 color_idx = cpu_mem_read(cpu, i);
		SDL_Color current_color = color(color_idx);
		u8 b1 = current_color.r;
		u8 b2 = current_color.g;
		u8 b3 = current_color.b;

		if (frame[frame_idx] != b1 || frame[frame_idx + 1] != b2 || frame[frame_idx + 2] != b3) {
			frame[frame_idx] = b1;
			frame[frame_idx+1] = b2;
			frame[frame_idx+2] = b3;
			update = true;
		}
		frame_idx += 3;
	}

	return update;
}

static void game_cycle(cpu_t *cpu) {
	SDL_Event e;
	if(SDL_PollEvent(&e)) {
		switch (e.type) {
			case SDL_QUIT:
				SDL_Quit();
				exit(1);
				break;
			case SDL_KEYDOWN:
				switch (e.key.keysym.sym) {
					case SDLK_UP:
						cpu_mem_write(cpu, 0xff, 0x77);
						break;
					case SDLK_DOWN:
						cpu_mem_write(cpu, 0xff, 0x73);
						break;
					case SDLK_LEFT:
						cpu_mem_write(cpu, 0xff, 0x61);
						break;
					case SDLK_RIGHT:
						cpu_mem_write(cpu, 0xff, 0x64);
						break;
					default:
						break;
				}
				break;
			default:
				break;
		}
	}

	cpu_mem_write(cpu, 0xfe, (u8) (rand() % (16 - 1) + 1));

	if(read_screen_state(cpu, screen_state)) {
		SDL_UpdateTexture(texture, NULL, screen_state, 32 * 3);
		SDL_RenderCopy(renderer, texture, NULL, NULL);
		SDL_RenderPresent(renderer);
	}

}

static void panic_and_abort(const char *title, const char *text) {
	fprintf(stderr, "PANIC: %s ... %s\n", title, text);
    SDL_ShowSimpleMessageBox(SDL_MESSAGEBOX_ERROR, title, text, NULL);
    SDL_Quit();
    exit(1);
}

static void init_sdl2(void) {
	// init sdl2
	if (SDL_Init(SDL_INIT_EVERYTHING) == -1) {
        panic_and_abort("SDL_Init failed", SDL_GetError());
    }

	window = SDL_CreateWindow("Snake", SDL_WINDOWPOS_CENTERED, SDL_WINDOWPOS_CENTERED, 32 * 10, 32 * 10, SDL_WINDOW_SHOWN);
	if (!window) {
        panic_and_abort("SDL_CreateWindow failed", SDL_GetError());
    }

	renderer = SDL_CreateRenderer(window, -1, SDL_RENDERER_PRESENTVSYNC);
	if (!renderer) {
        panic_and_abort("SDL_CreateRenderer failed", SDL_GetError());
    }

	SDL_RenderSetScale(renderer, 10, 10);

	texture = SDL_CreateTexture(renderer, SDL_PIXELFORMAT_RGB24, SDL_TEXTUREACCESS_TARGET, 32, 32);
	if (!texture) {
        panic_and_abort("SDL_CreateTexture failed", SDL_GetError());
    }
}

static void close_sdl2(void) {
	SDL_DestroyWindow(window);
	SDL_DestroyRenderer(renderer);
	SDL_DestroyTexture(texture);
	SDL_Quit();
}

int main(int argc, char **argv) {
	cpu_t *cpu = cpu_init();
	init_sdl2();
	srand(time(NULL));
	size_t game_code_size = sizeof(game_code) / sizeof(game_code[0]);
	cpu_load(cpu, game_code, game_code_size);
	cpu_reset(cpu);
	cpu_run_with_callback(cpu, &game_cycle);
	cpu_free(cpu);
	close_sdl2();
	return 0;
}
